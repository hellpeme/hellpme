package com.example.vincius.myapplication;

public class MessageReport {

    String messagemReport;
    String username;

    public MessageReport() {
    }

    public String getMessagemReport() {
        return messagemReport;
    }

    public void setMessagemReport(String messagemReport) {
        this.messagemReport = messagemReport;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
