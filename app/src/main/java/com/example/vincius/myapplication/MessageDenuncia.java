package com.example.vincius.myapplication;

public class MessageDenuncia {
    String messagemDenuncia;
    String username;
    String userDenunciado;

    public String getMessagemDenuncia() {
        return messagemDenuncia;
    }

    public void setMessagemDenuncia(String messagemDenuncia) {
        this.messagemDenuncia = messagemDenuncia;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getUserDenunciado() {
        return userDenunciado;
    }

    public void setUserDenunciado(String userDenunciado) {
        this.userDenunciado = userDenunciado;
    }
}
